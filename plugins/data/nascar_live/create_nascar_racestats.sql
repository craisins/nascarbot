create table if not exists nascar_racestats(
	ID INTEGER PRIMARY KEY AUTOINCREMENT,
	RaceID INTEGER DEFAULT -1,
	HistoricalID INTEGER DEFAULT -1,
	AverageRestartSpeed REAL DEFAULT -1,
	AverageRunningPosition REAL DEFAULT -1,
	AverageSpeed REAL DEFAULT -1,
	BestLap INTEGER DEFAULT -1,
	BestSpeed REAL DEFAULT -1,
	BestTime REAL DEFAULT -1,
	CarMake TEXT DEFAULT '',
	CarNo INTEGER DEFAULT -1,
	ElapsedTime INTEGER DEFAULT -1,
	FastestLaps INTEGER DEFAULT -1,
	LapCount INTEGER DEFAULT -1,
	LastLapSpeed REAL DEFAULT -1,
	LastLapTime REAL DEFAULT -1,
	Passes INTEGER DEFAULT -1,
	PassingDifferential INTEGER DEFAULT -1,
	RaceRank INTEGER DEFAULT -1,
	ResultID INTEGER DEFAULT -1,
	ResultStatus INTEGER DEFAULT -1,
	Sponsor TEXT DEFAULT '',
	StartPosition INTEGER DEFAULT -1,
	TimesPassed INTEGER DEFAULT -1,
	SFDelta INTEGER DEFAULT -1,
	QualifyingStatus INTEGER DEFAULT -1,
	BonusPoints INTEGER DEFAULT -1,
	DeltaLeader INTEGER DEFAULT -1,
	FirstName TEXT DEFAULT '',
	LastName TEXT DEFAULT '',
	IsInChase INTEGER DEFAULT -1,
	MembershipID INTEGER DEFAULT -1,
	Points INTEGER DEFAULT -1,
	PointsPosition INTEGER DEFAULT -1,
	RunningPositionPoints INTEGER DEFAULT -1,
	is_on_track INTEGER DEFAULT -1
);